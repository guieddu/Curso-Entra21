dia = int(input("Digite um número de 1 a 7:"))

match dia:
    case 1:
        print(f"O dia {dia} da semana é Domingo")
    case 2:
        print(f"O dia {dia} da semana é Segunda-feira")
    case 3:
        print(f"O dia {dia} da semana é Terça-feira")
    case 4:
        print(f"O dia {dia} da semana é Quarta-feira")
    case 5:
        print(f"O dia {dia} da semana é Quinta-feira")
    case 6:
        print(f"O dia {dia} da semana é Sexta-feira")
    case 7:
        print(f"O dia {dia} da semana é Sábado")